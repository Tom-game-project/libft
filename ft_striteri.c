#include <stdio.h>

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	i;

	i = 0;
	if (!s || !f)
		return ;
	while (s[i])
	{
		// printf("%c %p",*s,s);
		f(i, &s[i]);
		i++;
	}
}
